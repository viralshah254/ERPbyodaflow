/**
 * Writes public/firebase-messaging-sw.js from NEXT_PUBLIC_FIREBASE_* in .env
 * so the service worker is stable in dev (no hot-reload / DevTools popups).
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const envPath = join(root, ".env");

function loadEnv(path) {
  const out = {};
  try {
    const text = readFileSync(path, "utf8");
    for (const line of text.split("\n")) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith("#")) continue;
      const eq = trimmed.indexOf("=");
      if (eq <= 0) continue;
      out[trimmed.slice(0, eq).trim()] = trimmed.slice(eq + 1).trim();
    }
  } catch {
    /* optional */
  }
  return out;
}

const env = { ...loadEnv(join(root, ".env.example")), ...loadEnv(envPath) };

const config = {
  apiKey: env.NEXT_PUBLIC_FIREBASE_API_KEY ?? "",
  authDomain: env.NEXT_PUBLIC_FIREBASE_AUTH_DOMAIN ?? "",
  projectId: env.NEXT_PUBLIC_FIREBASE_PROJECT_ID ?? "",
  storageBucket: env.NEXT_PUBLIC_FIREBASE_STORAGE_BUCKET ?? "",
  messagingSenderId: env.NEXT_PUBLIC_FIREBASE_MESSAGING_SENDER_ID ?? "",
  appId: env.NEXT_PUBLIC_FIREBASE_APP_ID ?? "",
};

const body = `/* Generated by scripts/generate-firebase-messaging-sw.mjs — do not edit by hand */
importScripts("https://www.gstatic.com/firebasejs/10.14.0/firebase-app-compat.js");
importScripts("https://www.gstatic.com/firebasejs/10.14.0/firebase-messaging-compat.js");

self.addEventListener("install", function () {
  self.skipWaiting();
});

self.addEventListener("activate", function (event) {
  event.waitUntil(self.clients.claim());
});

firebase.initializeApp(${JSON.stringify(config)});
const messaging = firebase.messaging();

messaging.onBackgroundMessage(function (payload) {
  var title =
    (payload.notification && payload.notification.title) ||
    (payload.data && payload.data.title) ||
    "OdaFlow";
  var body =
    (payload.notification && payload.notification.body) ||
    (payload.data && payload.data.body) ||
    "";
  var routeWeb =
    (payload.data && payload.data.routeWeb) ||
    (payload.data &&
    payload.data.dedupeKey &&
    String(payload.data.dedupeKey).indexOf("password-reset-request:") === 0
      ? "/settings/users-roles"
      : "/automation/alerts");
  var notificationData = Object.assign({}, payload.data || {}, { routeWeb: routeWeb });

  return self.registration.showNotification(title, {
    body: body,
    icon: "/favicon.png",
    data: notificationData,
  });
});

self.addEventListener("notificationclick", function (event) {
  event.notification.close();
  var routeWeb =
    (event.notification.data && event.notification.data.routeWeb) ||
    (event.notification.data &&
    event.notification.data.dedupeKey &&
    String(event.notification.data.dedupeKey).indexOf("password-reset-request:") === 0
      ? "/settings/users-roles"
      : "/automation/alerts");
  event.waitUntil(
    clients.matchAll({ type: "window", includeUncontrolled: true }).then(function (list) {
      for (var i = 0; i < list.length; i++) {
        var client = list[i];
        if ("focus" in client) {
          client.navigate(routeWeb);
          return client.focus();
        }
      }
      if (clients.openWindow) return clients.openWindow(routeWeb);
    })
  );
});
`;

mkdirSync(join(root, "public"), { recursive: true });
const outPath = join(root, "public", "firebase-messaging-sw.js");
writeFileSync(outPath, body, "utf8");
console.log("[push] wrote", outPath);
